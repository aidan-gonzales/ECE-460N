	LDW R1, R1, #0 ; r1 = 0x8e43

	RSHFL R2, R1, #5 ; r2 = 0x0472
	RSHFA R3, R1, #5 ; r3 = 0xfc72
	LSHF R4, R1, #3 ; r4 = 0x7218

	LEA R5, ADDR
	LDW R5, R5, #0
	STW R2, R5, #0
	STW R3, R5, #1
	STW R4, R5, #-1
	
	STB R1, R5, #4
	STB R1, R5, #0
	STB R4, R5, #-1

